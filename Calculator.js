// ===== MAIN CALCULATION =====
function calculate() {
    // Get all input values
    const basePrice = parseFloat(document.getElementById('basePrice').value) || 0;
    const exchangeRate = parseFloat(document.getElementById('exchangeRate').value) || 0;
    const weight = parseFloat(document.getElementById('weight').value) || 0;
    const quantity = parseInt(document.getElementById('quantity').value) || 1;
    
    const platformFeePct = parseFloat(document.getElementById('platformFeePct').value) || 0;
    const platformFeeFix = parseFloat(document.getElementById('platformFeeFix').value) || 0;
    const platformFeeCurrency = document.getElementById('platformFeeCurrency').value;
    
    const convertFeePct = parseFloat(document.getElementById('convertFeePct').value) || 0;
    const acquiringPct = parseFloat(document.getElementById('acquiringPct').value) || 0;
    
    const chinaShippingCNY = parseFloat(document.getElementById('chinaShippingCNY').value) || 0;
    const warehouseHandling = parseFloat(document.getElementById('warehouseHandling').value) || 0;
    const exportPackaging = parseFloat(document.getElementById('exportPackaging').value) || 0;
    const consolidationFee = parseFloat(document.getElementById('consolidationFee').value) || 0;
    const chinaInsurancePct = parseFloat(document.getElementById('chinaInsurancePct').value) || 0;
    const chinaOther = parseFloat(document.getElementById('chinaOther').value) || 0;
    
    const borderCrossing = parseFloat(document.getElementById('borderCrossing').value) || 0;
    const exportDocs = parseFloat(document.getElementById('exportDocs').value) || 0;
    const dutyPct = parseFloat(document.getElementById('dutyPct').value) || 0;
    const vatPct = parseFloat(document.getElementById('vatPct').value) || 0;
    const customsFee = parseFloat(document.getElementById('customsFee').value) || 0;
    const brokerFee = parseFloat(document.getElementById('brokerFee').value) || 0;
    const markingFee = parseFloat(document.getElementById('markingFee').value) || 0;
    const certificationFee = parseFloat(document.getElementById('certificationFee').value) || 0;
    
    const ruShippingRate = parseFloat(document.getElementById('ruShippingRate').value) || 0;
    const ruShippingMin = parseFloat(document.getElementById('ruShippingMin').value) || 0;
    const ruInsurancePct = parseFloat(document.getElementById('ruInsurancePct').value) || 0;
    const lastMile = parseFloat(document.getElementById('lastMile').value) || 0;
    const finalPackaging = parseFloat(document.getElementById('finalPackaging').value) || 0;
    const returnReserve = parseFloat(document.getElementById('returnReserve').value) || 0;
    const ruOther = parseFloat(document.getElementById('ruOther').value) || 0;
    
    const opex = parseFloat(document.getElementById('opex').value) || 0;
    const supportReserve = parseFloat(document.getElementById('supportReserve').value) || 0;
    const taxSystem = document.getElementById('taxSystem').value;
    const extraTaxPct = parseFloat(document.getElementById('extraTaxPct').value) || 0;
    
    const aggregatorMode = document.getElementById('aggregatorMode').value;
    const aggregatorValue = parseFloat(document.getElementById('aggregatorValue').value) || 0;
    
    const targetMode = document.querySelector('input[name="targetMode"]:checked').value;
    const targetProfit = parseFloat(document.getElementById('targetProfit').value) || 0;
    const targetMarginPct = parseFloat(document.getElementById('targetMarginPct').value) || 0;
    const targetPrice = parseFloat(document.getElementById('targetPrice').value) || 0;
    const minMarginPct = parseFloat(document.getElementById('minMarginPct').value) || 0;
    
    // Calculations
    const productCostRUB = basePrice * exchangeRate * quantity;
    
    let platformFeeRUB = (basePrice * platformFeePct / 100) * exchangeRate * quantity;
    if (platformFeeFix > 0) {
        if (platformFeeCurrency === 'CNY') {
            platformFeeRUB += platformFeeFix * exchangeRate * quantity;
        } else if (platformFeeCurrency === 'USD') {
            platformFeeRUB += platformFeeFix * 90 * quantity;
        } else {
            platformFeeRUB += platformFeeFix * quantity;
        }
    }
    
    const convertFeeRUB = (productCostRUB + platformFeeRUB) * convertFeePct / 100;
    
    const chinaShippingRUB = chinaShippingCNY * exchangeRate * quantity;
    const chinaLogisticsTotal = chinaShippingRUB + warehouseHandling * quantity + 
                                exportPackaging * quantity + consolidationFee + 
                                (productCostRUB * chinaInsurancePct / 100) + chinaOther;
    
    const customsBase = productCostRUB + chinaShippingRUB;
    const dutyAmount = customsBase * dutyPct / 100;
    const vatBase = customsBase + dutyAmount;
    const vatAmount = vatBase * vatPct / 100;
    const customsTotal = dutyAmount + vatAmount + customsFee + brokerFee + markingFee + certificationFee;
    
    const chargeableWeight = Math.max(weight, calculateVolumetricWeight());
    let ruShippingCost = chargeableWeight * ruShippingRate * quantity;
    if (ruShippingCost < ruShippingMin) ruShippingCost = ruShippingMin;
    
    const ruInsuranceCost = productCostRUB * ruInsurancePct / 100;
    const ruLogisticsTotal = ruShippingCost + ruInsuranceCost + lastMile + 
                             finalPackaging + returnReserve + ruOther;
    
    const hardCost = productCostRUB + platformFeeRUB + convertFeeRUB + 
                     chinaLogisticsTotal + customsTotal + ruLogisticsTotal + 
                     opex + supportReserve;
    
    let aggregatorCommission = 0;
    if (aggregatorMode === 'fixed') {
        aggregatorCommission = aggregatorValue;
    } else if (aggregatorMode === 'pct_delivery') {
        aggregatorCommission = (chinaLogisticsTotal + ruLogisticsTotal) * aggregatorValue / 100;
    }
    
    // Final calculation based on mode
    let finalPrice, netProfit, margin, roi;
    
    if (calculatorState.mode === 'profit') {
        let targetValue = 0;
        if (targetMode === 'amount') {
            targetValue = targetProfit;
        } else {
            const estimatedPrice = hardCost / (1 - targetMarginPct/100 - acquiringPct/100);
            targetValue = estimatedPrice * targetMarginPct / 100;
        }
        
        finalPrice = (hardCost + targetValue + aggregatorCommission) / (1 - acquiringPct / 100);
        
        const acquiringCost = finalPrice * acquiringPct / 100;
        const revenue = finalPrice - acquiringCost;
        const profitBeforeTax = revenue - hardCost - aggregatorCommission;
        
        let tax = 0;
        if (taxSystem === 'usn6') {
            tax = finalPrice * 0.06;
        } else if (taxSystem === 'usn15') {
            tax = Math.max(0, profitBeforeTax) * 0.15;
        } else if (taxSystem === 'osno') {
            tax = Math.max(0, profitBeforeTax) * 0.20 + vatAmount;
        }
        tax += finalPrice * extraTaxPct / 100;
        
        if (aggregatorMode === 'pct_order') {
            aggregatorCommission = finalPrice * aggregatorValue / 100;
        }
        
        netProfit = profitBeforeTax - tax - aggregatorCommission;
        margin = finalPrice > 0 ? (netProfit / finalPrice * 100) : 0;
        roi = hardCost > 0 ? (netProfit / hardCost * 100) : 0;
        
    } else {
        finalPrice = targetPrice;
        
        const acquiringCost = finalPrice * acquiringPct / 100;
        const revenue = finalPrice - acquiringCost;
        
        if (aggregatorMode === 'pct_order') {
            aggregatorCommission = finalPrice * aggregatorValue / 100;
        }
        
        const profitBeforeTax = revenue - hardCost - aggregatorCommission;
        
        let tax = 0;
        if (taxSystem === 'usn6') {
            tax = finalPrice * 0.06;
        } else if (taxSystem === 'usn15') {
            tax = Math.max(0, profitBeforeTax) * 0.15;
        } else if (taxSystem === 'osno') {
            tax = Math.max(0, profitBeforeTax) * 0.20 + vatAmount;
        }
        tax += finalPrice * extraTaxPct / 100;
        // Исправленная логика:
if (aggregatorMode !== 'none') {
    // Вы агрегатор — комиссия это ВАШ ДОХОД
    netProfit = profitBeforeTax - tax + aggregatorCommission;
} else {
    // Вы сами везёте/продаёте — без комиссии
    netProfit = profitBeforeTax - tax;
}
        margin = finalPrice > 0 ? (netProfit / finalPrice * 100) : 0;
        roi = hardCost > 0 ? (netProfit / hardCost * 100) : 0;
    }
    
    // Update UI
    updateUI({
        productCostRUB, platformFeeRUB, convertFeeRUB, chinaLogisticsTotal,
        customsBase, dutyAmount, vatBase, vatAmount, customsTotal,
        ruLogisticsTotal, hardCost, aggregatorCommission,
        finalPrice, acquiringPct, netProfit, margin, roi,
        dutyPct, vatPct, taxSystem
    });
    
    generateWarnings(margin, minMarginPct, netProfit);
}

// ===== HELPER FUNCTIONS =====
function calculateVolumetricWeight() {
    const dims = document.getElementById('dimensions').value;
    const parts = dims.split(/[×x*]/).map(p => parseFloat(p.trim()) || 0);
    if (parts.length !== 3) return 0;
    const [l, w, h] = parts;
    return (l * w * h) / 5000;
}

